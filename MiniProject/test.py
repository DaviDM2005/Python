n = int(input("Input number of iterations: "))  # შევიტანოთ იტერაციების რაოდენობა

sum = 0                 # გამოვაცხადოთ ცვლადი sum და გავუკეთოდ მას ინიციალიზაცია

for i in range(n + 1):  # ციკლი რომელიც მოიცავს ბოლო მნიშვნელობასაც (n + 1)
    sum += i ** 2       # მიმდინარე ნაბიჯის აყვანა კუბში და აჯამვა
    
print("Cubes sum is: ", sum)